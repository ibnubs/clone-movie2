import React from 'react';
import LoginForm from '../components/Login';

class ModalLogin extends React.Component {
    state = { visible: false };

    showModal = () => {
        this.setState({
            visible: true,
        });
    };

    handleOk = e => {
        console.log(e);
        this.setState({
            visible: false,
        });
    };

    handleCancel = e => {
        console.log(e);
        this.setState({
            visible: false,
        });
    };

    render() {
        return (
            <div>
                <LoginForm />
            </div>
        )
    }
}

export default ModalLogin;